/* Generates supabase/01-course-seed.sql from js/curriculum.js — same pattern
   as Piano Lab. Run:  node tools/gen-lms-seed.js  (from the aural lab folder).
   Additive: the SQL touches only the 'aural-lab' course. */
const fs=require("fs"),path=require("path");
global.window=undefined;
const CUR=require(path.join(__dirname,"..","js","curriculum.js"));
const q=s=>"$q$"+s+"$q$";
let sql="-- Aural Lab curriculum seed — GENERATED by tools/gen-lms-seed.js. Do not edit by hand.\n"+
  "-- Additive: touches only the 'aural-lab' course; existing courses are untouched.\n"+
  "insert into courses(id,title,active) values ('aural-lab','Aural Lab',true)\n"+
  "  on conflict (id) do update set title=excluded.title;\n";
CUR.units.forEach(u=>{
  sql+=`insert into curriculum_units(course_id,unit,title) values ('aural-lab',${u.unit},${q(u.title)}) on conflict (course_id,unit) do update set title=excluded.title;\n`;
});
CUR.lessons.forEach(l=>{
  const status=l.status==="ready"?"active":"draft";
  sql+=`insert into curriculum_items(course_id,item_id,unit,label,title,route,lbd_ids,lbd_count,has_quiz,status,version) values ('aural-lab',${l.item},${l.unit},${q(l.num)},${q(l.title)},${q(l.route)},'["${l.id}"]'::jsonb,1,false,'${status}',1) on conflict (course_id,item_id) do update set unit=excluded.unit,label=excluded.label,title=excluded.title,route=excluded.route,lbd_ids=excluded.lbd_ids,lbd_count=excluded.lbd_count,has_quiz=excluded.has_quiz,status=excluded.status,version=excluded.version;\n`;
});
const out=path.join(__dirname,"..","supabase","01-course-seed.sql");
fs.mkdirSync(path.dirname(out),{recursive:true});
fs.writeFileSync(out,sql);
console.log("wrote",out,"—",CUR.lessons.length,"lessons,",CUR.units.length,"units");
